        if pressure not in self.superheated_tables:
            raise ValueError(f"Pressure {pressure} bar not available.")
        
        table = self.superheated_tables[pressure]
        sorted_temps = sorted(table.keys())
        
        # Exact match case
        if temp in table:
            return table[temp]
        
        # Find nearest temps for interpolation
        lower = None
        higher = None
        for t in sorted_temps:
            if t < temp:
                lower = t
            elif t > temp:
                higher = t
                break
        
        if lower is None or higher is None:
            raise ValueError(f"Temperature {temp}°C out of range for {pressure} bar.")
        
        
        v1, u1, h1, s1 = table[lower]
